  function updateCartQuantity(productId, quantity) {
      fetch('index.php?act=updateCart', {
          method: 'POST',
          headers: {
              'Content-Type': 'application/json'
          },
          body: JSON.stringify({
              id: productId,
              quantity: quantity
          })
      })
      .then(response => response.json())
      .then(data => {
          if (data.success) {
              // Cập nhật tổng tiền giỏ hàng
              updateTotalPrice(data.cartTotal);
